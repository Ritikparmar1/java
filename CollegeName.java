class CollegeName {
    public static void main(String[] args) {
        String college = "sait university";

        System.out.println("Displaying college name character by character:");
        for (int i = 0; i < college.length(); i++) {
            System.out.println(college.charAt(i));
        }
        System.out.println("Name:- Ritik Parmar");
        System.out.println("Roll no:- 0873CS241136");
    }
}