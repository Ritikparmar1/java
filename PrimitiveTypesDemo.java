public class PrimitiveTypesDemo {
    public static void main(String[] args) {
        // Integer types
        byte byteVar = 127;               // 8-bit
        short shortVar = 32000;           // 16-bit
        int intVar = 100000;              // 32-bit
        long longVar = 10000000000L;      // 64-bit

        // Floating-point types
        float floatVar = 3.14f;           // 32-bit
        double doubleVar = 3.14159265359; // 64-bit

        // Character type
        char charVar = 'A';               // 16-bit Unicode

        // Boolean type
        boolean booleanVar = true;        // true or false

        // Print all primitive types
        System.out.println("byte: " + byteVar);
        System.out.println("short: " + shortVar);
        System.out.println("int: " + intVar);
        System.out.println("long: " + longVar);
        System.out.println("float: " + floatVar);
        System.out.println("double: " + doubleVar);
        System.out.println("char: " + charVar);
        System.out.println("boolean: " + booleanVar);
        // Print personal info
        System.out.println("Name:- Ritik Parmar");
        System.out.println("Roll no.:- 0873CS241136");
        System.out.println("Department:- Computer Science");
 }
}
